"""
Data models for the SupportSphere AI evaluation framework.
"""

from __future__ import annotations

from dataclasses import dataclass
from dataclasses import field
from typing import Any, Dict, List, Optional


@dataclass(frozen=True)
class EvaluationSample:
    """
    Represents a ground truth reference sample from the evaluation dataset.
    """
    issue: str
    subject: str
    company: str
    expected_response: str
    expected_request_type: str
    expected_product_area: str
    expected_status: str
    metadata: Dict[str, Any] = field(default_factory=dict)


@dataclass
class ClassificationPrediction:
    """
    Stores classification outputs generated by the SupportAgent.
    """
    predicted_request_type: str
    predicted_product_area: str
    predicted_status: str
    request_type_accuracy: int  # 1 if correct, 0 if incorrect
    product_area_accuracy: int
    status_accuracy: int


@dataclass(frozen=True)
class ClassificationMetrics:
    """
    Aggregated classification performance metrics.
    """
    accuracy: float
    precision_macro: float
    recall_macro: float
    f1_macro: float
    precision_weighted: float
    recall_weighted: float
    f1_weighted: float
    confusion_matrix: List[List[int]]
    per_label_metrics: Dict[str, Dict[str, float]]


@dataclass(frozen=True)
class RagasMetrics:
    """
    Ragas RAG performance scores.
    """
    context_precision: Optional[float] = None
    context_recall: Optional[float] = None
    faithfulness: Optional[float] = None
    answer_correctness: Optional[float] = None
    answer_relevancy: Optional[float] = None


@dataclass
class EvaluationResult:
    """
    Complete evaluation details for a single ticket execution.
    """
    sample: EvaluationSample
    generated_response: str
    retrieved_contexts: List[str]
    prediction: ClassificationPrediction
    ragas_metrics: RagasMetrics
    latency: float
    num_chunks: int
    
    # Decision Gate fields (NEW)
    retrieval_required: bool = True
    normalized_issue: str = ""
    normalized_subject: str = ""
    routing_reason: str = ""
    confidence: float = 1.0


@dataclass(frozen=True)
class EvaluationSummary:
    """
    Final aggregated metrics and metadata summary for an evaluation run.
    """
    total_tickets: int
    avg_latency: float
    avg_chunks: float
    elapsed_time_seconds: float
    
    # Classification
    request_type_metrics: ClassificationMetrics
    product_area_metrics: ClassificationMetrics
    status_metrics: ClassificationMetrics
    
    # Routing (NEW)
    retrieval_decision_accuracy: float
    escalation_accuracy: float
    out_of_scope_accuracy: float
    greeting_accuracy: float
    retrieval_skip_rate: float
    
    # Ragas Averages
    avg_context_precision: Optional[float]
    avg_context_recall: Optional[float]
    avg_faithfulness: Optional[float]
    avg_answer_correctness: Optional[float]
    avg_answer_relevancy: Optional[float]
    
    # Config details
    provider: str
    model: str
    search_mode: str
    reranker: str
    top_k: int
    evaluation_error: Optional[str] = None
