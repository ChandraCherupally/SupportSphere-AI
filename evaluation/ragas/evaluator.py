"""
Ragas evaluator orchestration for the SupportSphere AI evaluation framework.
"""

from __future__ import annotations

import logging
from typing import Any, Dict, List, Optional

import pandas as pd
from datasets import Dataset
from ragas import evaluate
from ragas.embeddings import LangchainEmbeddingsWrapper
from ragas.llms import LangchainLLMWrapper

from evaluation.models import RagasMetrics
from evaluation.ragas.metrics import get_ragas_metrics

logger = logging.getLogger(__name__)


class RagasEvaluator:
    """
    Orchestrates execution of Ragas evaluation metrics on lists of predictions and contexts.
    """

    def __init__(self, llm_model: Any, embedding_model: Any) -> None:
        """
        Initialize the evaluator with LangChain models.

        Args:
            llm_model: LangChain chat model.
            embedding_model: LangChain embeddings model.
        """
        logger.info("Initializing Ragas wrappers for LLM and Embeddings.")
        self.ragas_llm = LangchainLLMWrapper(llm_model)
        self.ragas_embeddings = LangchainEmbeddingsWrapper(embedding_model)
        self.metrics = get_ragas_metrics()

    def evaluate_batch(
        self,
        questions: List[str],
        answers: List[str],
        contexts: List[List[str]],
        ground_truths: List[str],
    ) -> List[RagasMetrics]:
        """
        Executes Ragas metrics calculation over a batch of inputs.

        Args:
            questions: List of customer issues/inquiries.
            answers: List of responses generated by the SupportAgent.
            contexts: Nested list of retrieved context texts.
            ground_truths: List of ground truth answers.

        Returns:
            List of RagasMetrics containing scores for each sample.
        """
        if not (len(questions) == len(answers) == len(contexts) == len(ground_truths)):
            error_msg = (
                f"Batch size mismatch: questions={len(questions)}, answers={len(answers)}, "
                f"contexts={len(contexts)}, ground_truths={len(ground_truths)}."
            )
            logger.error(error_msg)
            raise ValueError(error_msg)

        if not questions:
            logger.warning("Empty batch passed to Ragas evaluation.")
            return []

        logger.info(f"Preparing Ragas evaluation dataset for batch size of {len(questions)}.")

        # Ensure contexts are lists of strings
        normalized_contexts = []
        for ctx in contexts:
            if isinstance(ctx, list):
                normalized_contexts.append([str(c) for c in ctx])
            else:
                normalized_contexts.append([str(ctx)])

        dataset_dict = {
            "question": questions,
            "answer": answers,
            "contexts": normalized_contexts,
            "ground_truth": ground_truths,
        }

        try:
            logger.info("Creating datasets.Dataset instance.")
            dataset = Dataset.from_dict(dataset_dict)
            
            logger.info("Executing Ragas evaluation pipeline.")
            results = evaluate(
                dataset=dataset,
                metrics=self.metrics,
                llm=self.ragas_llm,
                embeddings=self.ragas_embeddings,
            )
            
            logger.info("Ragas evaluation completed successfully. Formatting scores.")
            
            # Map results to RagasMetrics objects
            df_scores = results.to_pandas()
            scores_list: List[RagasMetrics] = []
            
            def _safe(row: Any, key: str) -> Optional[float]:
                """Return float score or 0.0 for NaN/None (e.g. OutputParsingFailure)."""
                val = row.get(key)
                if val is None or (hasattr(val, '__class__') and val.__class__.__name__ == 'float' and pd.isna(val)):
                    return 0.0
                try:
                    f = float(val)
                    return 0.0 if pd.isna(f) else f
                except (TypeError, ValueError):
                    return 0.0

            for _, row in df_scores.iterrows():
                scores_list.append(
                    RagasMetrics(
                        context_precision=_safe(row, "context_precision"),
                        context_recall=_safe(row, "context_recall"),
                        faithfulness=_safe(row, "faithfulness"),
                        answer_correctness=_safe(row, "answer_correctness"),
                        answer_relevancy=_safe(row, "answer_relevancy"),
                    )
                )
                
            return scores_list

        except Exception as e:
            logger.error(f"Ragas evaluation failed: {e}", exc_info=True)
            # Safe fallback returning empty metrics
            return [RagasMetrics() for _ in range(len(questions))]
